// If..Else

// Se a hora estiver entre 06:00 até 12:00 exibir : bom dia!
// se estiver entre 12:00 até as 18:00 exibir : boa tarde!
// Caso contrário boa noite!

let hora = 10;

if (hora > 6 &&  hora < 12) {

    console.log("Bom Dia!!")


}else if(hora < 12 && hora > 18){

    console.log("Boa Tarde!!")

} 
else{   

    console.log("Boa Noite!!")



}
